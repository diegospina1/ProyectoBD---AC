        TelefonosDao daoTel = new TelefonosDao();
        Telefonos tel;
        tel = daoTel.buscarTelM(Integer.parseInt(txtBDoc.getText()));
        String numero = tel.getNumero();
        daoTel.DarDeBajaTel(numero);
        
        dao.DarDeBajaD(Integer.parseInt(txtBDoc.getText()));
         
