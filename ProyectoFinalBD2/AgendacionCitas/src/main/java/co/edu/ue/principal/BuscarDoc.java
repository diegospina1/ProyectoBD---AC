    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       
        DoctoresDao dao = new DoctoresDao();
        TelefonosDao daoTel = new TelefonosDao();
        Doctores doc = new Doctores(0, "", "", "");
        Telefonos tel = new Telefonos("", 0);
        
        doc = dao.buscarDoctor(Integer.parseInt(txtBDoc.getText()));
        tel = daoTel.buscarTelM(Integer.parseInt(txtBDoc.getText()));
        
        txtApmM.setText(doc.getApellido_materno());
        txtAppM.setText(doc.getApellido_paterno());
        txtNombreM.setText(doc.getNombre_med());
        txtDocumentoM.setText(String.valueOf(doc.getDocumento_med()));
        txtTel.setText(tel.getNumero());
        if(tel.getCategoria() == 1){
            txtTipoTelM.setText("CELULAR");
        }
        else if(tel.getCategoria() == 2){
            txtTipoTelM.setText("FIJO");
        }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModActionPerformed
        
        DoctoresDao dao = new DoctoresDao();
        TelefonosDao daoTel = new TelefonosDao();
        Doctores doctor = new Doctores(Integer.parseInt(txtBDoc.getText()),txtNombreM1.getText()
                ,txtAppM1.getText(),txtApmM1.getText());
        
        String telViejo = txtTel.getText();
        
        Telefonos telNuevo =  new Telefonos(txtTel1.getText(), cbxTipoTelM1.getSelectedIndex());
       
        dao.modificarDoctor(doctor);
        daoTel.modificarTel(telNuevo, telViejo);
        
        clearFields();
         
    }//GEN-LAST:event_btnModActionPerformed

